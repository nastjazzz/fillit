#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*ptr;
	size_t			i;

	ptr = (unsigned char *)b;
	i = 0;
	while (len > 0)
	{
		*ptr = c;
		ptr++;
		len--;
	}
	return ((unsigned char *)b);
}
